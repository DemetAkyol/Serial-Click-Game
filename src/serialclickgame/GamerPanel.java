    @Override
    public void starteGame(String received) {
         btnSend.setEnabled(false);
        Info.shape2.clear();
        received = received.replace("Start+", "");
        String[] receivedToken = received.split(";");
        for (String item : receivedToken) {
            String[] mParsed = item.split("\\+");
            int width = Integer.parseInt(mParsed[1]);
            int height = Integer.parseInt(mParsed[0]);
            int x1 = Integer.parseInt(mParsed[2]);
            int y1 = Integer.parseInt(mParsed[3]);
            int x2 = Integer.parseInt(mParsed[4]);
            int y2 = Integer.parseInt(mParsed[5]);
            int randomShape = Integer.parseInt(mParsed[6]);
            int firstColor = Integer.parseInt(mParsed[7]);
            int secondColor = Integer.parseInt(mParsed[8]);
            boolean cyclic = Boolean.parseBoolean(mParsed[9]);

            Shape shapeEx = new Shape(randomShape, x1, y1, x2, y2, firstColor, secondColor, cyclic);
            Info.shape2.add(shapeEx);
        }
        if (!Info.shape2.isEmpty()) {
            showShapes(500, 500);
        }

        //jPanel2= new PanelDraw(width,height,x1,y1,x2,y2,firstColor,secondColor,randomShape,shape,cyclic);
    }

